//定时器3中断服务函数
void TIM6_DAC_IRQHandler(void)
{
   
	if(__HAL_TIM_GET_FLAG(&htim6,TIM_FLAG_UPDATE)!=RESET)    //更新中断
    {
        __HAL_TIM_CLEAR_IT(&htim6,TIM_IT_UPDATE);            //清除中断
        USART1_RX_STA|=1<<15;	                                    //标记接收完成
        __HAL_TIM_DISABLE(&htim6);                           //关闭定时器7 
    }
		
}

